function changeText(card) {
  var description = card.querySelector(".card1_text");
  card.dataset.originalText = description.textContent;
  description.textContent =
    "The gradual information about atomic behaviour and more more more more...";
}
function resetText(card) {
  var description = card.querySelector(".card1_text");
  description.textContent = card.dataset.originalText;
}

